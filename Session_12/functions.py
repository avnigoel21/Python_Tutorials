def percentage(marks):
    return (sum(marks)/400)*100


marks1 = [45, 78, 86, 77]
percentage1 = percentage(marks1)

marks2 = [75, 67, 45, 12]
percentage2 = percentage(marks2)

print(percentage1 , percentage2)


